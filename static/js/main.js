function validateName() {

  var name = document.getElementById('contact-name').value;

  if(name.length == 0) {

    producePrompt('Name is required', 'name-error' , 'red')
    return false;

  }
  producePrompt('','name-error','white')
  return true;

}

function validateEmail () {

  var email = document.getElementById('contact-email').value;

  if(email.length == 0) {

    producePrompt('Email Invalid','email-error', 'red');
    return false;

  }

  if(!email.match(/^[A-Za-z\._\-[0-9]*[@][A-Za-z]*[\.][a-z]{2,4}$/)) {

    producePrompt('Email Invalid', 'email-error', 'red');
    return false;

  }
  document.getElementById('email-error').style.display = "none";
  return true;

}

function validatePhone() {

  var phone = document.getElementById('contact-phone').value;
    if(phone.length == 0) {
      return true;
    }

    if(phone.length < 10) {
      producePrompt('Include area code.', 'phone-error', 'red');
      return false;
    }

    if(!phone.match(/^[\+]?[(]?[0-9]{3}[)]?[-\s\.]?[0-9]{3}[-\s\.]?[0-9]{4,6}$/)) {
      producePrompt('Only digits, please.' ,'phone-error', 'red');
      return false;
    }
   document.getElementById('phone-error').style.display = "none";
   return true;

}

function validateMessage() {
  var message = document.getElementById('contact-message').value;
  var required = 15;
  var max_limit = 200;
  var left = required - message.length;
  var limit = message.length - max_limit;
  if (left > 0) {
    producePrompt(left + ' more characters required','message-error','red');
    return false;
  }
  if (limit > 0){
    producePrompt('Maximum 150 characters allowed', 'message-error', 'red');
    return false;
  }
  document.getElementById('message-error').style.display = "none";
  return true;
}

function validateCaptcha()
{
    var v = grecaptcha.getResponse();
    if (v.length==0)
    {
        producePrompt('Please Validate Captcha', 'captcha-error','red');
        return false;
    }
    else
    {
        document.getElementById('captcha-error').style.display = "none";
//        grecaptcha.reset();
        return true;
    }
}


function validateForm() {
    if (!validateName() || !validateEmail() || !validatePhone() || !validateMessage()  || !validateCaptcha()) {
        return false;
    }
}
function producePrompt(message, promptLocation, color) {
  document.getElementById(promptLocation).innerHTML = message;
  document.getElementById(promptLocation).style.color = color;
}